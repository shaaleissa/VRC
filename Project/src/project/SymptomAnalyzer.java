    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String age;
        Statement st;
 String ageQuery="SELECT DATE_FORMAT(FROM_DAYS(DATEDIFF(now(),User.BDate)), '%Y')+0 AS Age FROM User WHERE SSN = "+SignIn.acc.getUserName();
  ResultSet rs;
    try {
        st=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
        rs=st.executeQuery(ageQuery);
        
        if(rs.next()){
            ageTF.setText(rs.getString("Age"));
            age=ageTF.getText();
            if(Double.parseDouble(age) >=60) 
    JOptionPane.showMessageDialog(this, "Vaccinating at your age is very risky.");
else if (Double.parseDouble(age)<12)
        JOptionPane.showMessageDialog(this, "You are too young to vaccinate.");
        }
        
    } catch (SQLException ex) {
        Logger.getLogger(SymptomAnalyzer.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

