    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        String age;
age = ageTF.getText();

        if (RadioButtonYES.isSelected())
           if (jCheckBox1.isSelected() || jCheckBox2.isSelected() || jCheckBox3.isSelected() || jCheckBox4.isSelected() || jCheckBox5.isSelected())
               JOptionPane.showMessageDialog(null, "You are denied an appointment. We urge you to get a Covid-19 test and quarantine.");
        
        //JframePage is the name of the next page(Jframe), Jf2 is the variable.
                Appointment Jf2 = new Appointment();
       if (RadioButtonNO.isSelected()){
           if (jCheckBoxNONE.isSelected()){
               if (Double.parseDouble(age) >=12 && Double.parseDouble(age) <60)
                   Jf2.show(); //to show the next page
                   dispose();} 
       
       } // to close the previous page after opening the next.
       if (RadioButtonNO.isSelected()){
           if(jCheckBox1.isSelected() || jCheckBox2.isSelected() || jCheckBox3.isSelected() || jCheckBox4.isSelected() || jCheckBox5.isSelected())
           JOptionPane.showMessageDialog(this, "It is best for you to wait 5 days before booking a vaccine, to check for any symptoms."); 
           this.setVisible(false);
           Jf2.setVisible(true);
       }
     
           
       
       if (RadioButtonNO.isSelected())
           if (jCheckBoxNONE.isSelected())
               if(Double.parseDouble(age)>=60)
              JOptionPane.showMessageDialog(this, "Your age poses a high risk."); 
       
       if (RadioButtonNO.isSelected())
           if (jCheckBoxNONE.isSelected())
               if(Double.parseDouble(age)<12)
              JOptionPane.showMessageDialog(this, "You are too young to vaccinate."); 
       
            
       
       
       if (RadioButtonYES.isSelected())
           if (jCheckBoxNONE.isSelected())
              JOptionPane.showMessageDialog(this, "It is best for you to wait 5 days before booking a vaccine, to check for any symptoms."); 
      
       
        
    }//GEN-LAST:event_SubmitButtonActionPerformed

